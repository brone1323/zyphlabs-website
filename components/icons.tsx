// Monoline SVG icons — 24×24 viewBox, stroke-based, no fill
// Replaces emoji role icons across the site

type P = { size?: number; color?: string; className?: string }

function Svg({ size = 20, color = 'currentColor', className, children }: P & { children: React.ReactNode }) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke={color}
      strokeWidth="1.5"
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
      aria-hidden="true"
    >
      {children}
    </svg>
  )
}

